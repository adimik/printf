#include "ft_printf.h"

int	ft_print_s(char *c)
{
	int	i;

	i = 0;
	if (!c)
		return (ft_print_s("(null)"));
	while (*c)
	{
		i += write(1, c, 1);
		c++;
	}
	return (i);
}
