#include "ft_printf.h"

int	ft_print_u(unsigned int i)
{
	int	c;

	c = 0;
	if (i < 10)
		c += ft_print_c(i + 48);
	else
	{
		c += ft_print_u(i / 10);
		c += ft_print_u(i % 10);
	}
	return (c);
}
